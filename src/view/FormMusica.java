    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        ResultSet rs = null;
        int i = 0;
        try {
            rs = this.musica.selectByName(jTextField1.getText());
        } catch (SQLException ex) {
            Logger.getLogger(FormPrincipal.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {
            while(rs.next()){
                i++;
            }
        } catch (SQLException ex) {
            Logger.getLogger(FormPrincipal.class.getName()).log(Level.SEVERE, null, ex);
        }
        String nome_artista = (String) jComboBox1.getSelectedItem();
        String nome_album = (String) jComboBox2.getSelectedItem();
        String nome_musica = jTextField1.getText();
        String nome_diretorio = jTextField2.getText();
        String avaliacao = "1";
        Enumeration elements = buttonGroup1.getElements();
        while (elements.hasMoreElements()) {
            AbstractButton button = (AbstractButton)elements.nextElement();
            if (button.isSelected()) {
                avaliacao = button.getText();
            }
        }
        if(i > 0){
            try {
                this.musica.editMusica(nome_artista, nome_album, nome_musica, nome_diretorio, avaliacao);
            } catch (SQLException ex) {
                Logger.getLogger(FormMusica.class.getName()).log(Level.SEVERE, null, ex);
            }
        }else{
            try {
               this.musica.addMusica(nome_artista, nome_album, nome_musica, nome_diretorio, avaliacao);
            } catch (SQLException ex) {
                Logger.getLogger(FormMusica.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        FormPrincipal p = new FormPrincipal();
        p.setVisible(true);
        try {
            p.listarMusicas();
        } catch (SQLException ex) {
            Logger.getLogger(FormPrincipal.class.getName()).log(Level.SEVERE, null, ex);
        }
        this.setVisible(false);
    }//GEN-LAST:event_jButton1ActionPerformed

