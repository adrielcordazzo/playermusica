    public void obterDadosMusica(String mus) throws SQLException{
        ResultSet rs = null;
        try {
            rs = this.musica.selectByName(mus);
        } catch (SQLException ex) {
            Logger.getLogger(FormPrincipal.class.getName()).log(Level.SEVERE, null, ex);
        }

        while(rs.next()){
            jComboBox1.setSelectedItem(rs.getString("artistaNome"));
            jComboBox2.setSelectedItem(rs.getString("albumNome"));
            jTextField1.setText(rs.getString("nome"));
            jTextField2.setText(rs.getString("diretorio"));    
            Enumeration elements = buttonGroup1.getElements();
            while (elements.hasMoreElements()) {
                AbstractButton button = (AbstractButton)elements.nextElement();
                if (button.getText().equals(rs.getString("avaliacao"))) {
                    button.setSelected(true);
                }
            } 
        }
    }
